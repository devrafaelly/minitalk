#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	res;
	long	nb;

	nb = n;
	if (nb < 0)
	{
		nb *= -1;
		ft_putchar_fd('-', fd);
	}
	if (nb > 9)
	{
		ft_putnbr_fd(nb / 10, fd);
		ft_putnbr_fd(nb % 10, fd);
	}
	else
	{
		res = nb + '0';
		ft_putchar_fd(res, fd);
	}
}
